#include "main.h"
/**
 * Addition - sums two integers
 * @a: integer
 * @b: integer
 *
 * Return: addition of the two integers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * Subtraction - subtracts two integers
 * @a: integer
 * @b: integer
 *
 * Return: differences of the two integers
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * Multiplication - multiplies two integers
 * @a: integer
 * @b: integer
 *
 * Return: multiplication of the two integers
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * Division - divides two integers
 * @a: integer
 * @b: integer
 *
 * Return: division of the two integers
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * Modulus - remains two integers after division
 * @a: integer
 * @b: integer
 *
 * Return: remainder of the two integers
 */
int mod(int a, int b)
{
        return (a % b);
}
